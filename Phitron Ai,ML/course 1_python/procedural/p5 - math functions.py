import math

pi = 3.14
x=1
y=2
z=3

# rounding a number
print(round(pi))

# ceil a number
print(math.ceil(pi))

# floor a number
print(math.floor(pi))

# abs a number
print(abs(pi))

# power a number
print(pow(pi,2))

# square root a number
print(math.sqrt(420))

# find max number
print(max(x,y,z))

# find min number
print(min(x,y,z))