# type casting = convert the data type of a value to another

x = 1 #int
y = 2.0 #float
z = "3" #string


# converting float to integer

y = int(y)

print(y)

# converting string to int

z = int(z)
print(z*3)

# converting int to float

x = float(x)
print(x)

# converting int,float to string for concetanation

print("x is "+str(x))

