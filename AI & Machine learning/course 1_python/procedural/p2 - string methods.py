# few methods for string 

name = "bro code"

# length of the variable
print(len(name))

# finding index of a letter in string 
print(name.find("b"))

# capitalize the first letter
print(name.capitalize())

# make string uppercase
print(name.upper())

# make string lowercase
print(name.lower())

#  checks if the string is digit
print(name.isdigit())

#  checks if the string is alphabetical
print(name.isalpha())

#  count number of given character in our string
print(name.count("o"))

#  replace characters
print(name.replace("o","a"))

# display a string multiple times
print(name*3)