# 2D lists = list of lists

drinks = ["coffee", "tea", "water"]
dinner = ["pizza", "pasta", "salad"]
dessert = ["cake", "ice cream", "pie"]

menu = [drinks, dinner, dessert]

print(menu)  # prints all the  3 lists
print(menu[0])  # prints the first list (drinks)
print(menu[0][0])  # prints the first item in the first list (coffee)
